/// Version 2 of dataset layout APIs (experimental).
///
/// This package contains the second iteration of dataset layout specifications,
/// providing an improved design for organizing and accessing test dataset structures.
/// This is an evolution of the `layout` package with enhanced capabilities.
///
/// ## Key Components
///
/// - {@link io.nosqlbench.vectordata.layoutv2.DSRoot}: Root dataset descriptor
/// - {@link io.nosqlbench.vectordata.layoutv2.DSProfile}: Dataset profile specification
/// - {@link io.nosqlbench.vectordata.layoutv2.DSProfileGroup}: Grouped profiles
/// - {@link io.nosqlbench.vectordata.layoutv2.DSSource}: Data source descriptor
/// - {@link io.nosqlbench.vectordata.layoutv2.DSView}: Dataset view specification
/// - {@link io.nosqlbench.vectordata.layoutv2.DSWindow}: Windowed dataset access
/// - {@link io.nosqlbench.vectordata.layoutv2.DSInterval}: Interval specification
