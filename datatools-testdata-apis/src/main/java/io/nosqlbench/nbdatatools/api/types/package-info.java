/// Core type interfaces and utilities for data tools.
///
/// This package provides fundamental interfaces that define common capabilities across the data tools framework,
/// including naming, sizing, and enumeration. These interfaces are used throughout the system to provide
/// consistent access patterns.
///
/// ## Key Components
///
/// - {@link io.nosqlbench.nbdatatools.api.types.Named}: Elements with names
/// - {@link io.nosqlbench.nbdatatools.api.types.Sized}: Elements with determinable size
/// - {@link io.nosqlbench.nbdatatools.api.types.Enumerated}: Elements with ordinal positions
///
/// ## Sub-packages
///
/// - {@link io.nosqlbench.nbdatatools.api.types.terminal}: Terminal color support detection
/// - {@link io.nosqlbench.nbdatatools.api.types.bitimage}: Bit image manipulation for terminal graphics
package io.nosqlbench.nbdatatools.api.types;

